#include <stdlib.h>
#include <ctype.h>
#include "main.h"
/**
 * print_last_digit - computes the absolute value of an integer
 * @n: An input character
 * Return: always 0.
 */

int print_last_digit(int n)
{
int o = n % 10;
int p = o * 10;
int q = p + o;
char r = (char)q;
return ('r');
}
